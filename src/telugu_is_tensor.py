# తెలుగు టెన్సర్ తనిఖీ ఫంక్షన్

def టెన్సర్_ఉందా(వస్తువు):
    """
    ఈ ఫంక్షన్ ఒక వస్తువు టెన్సర్ అయినా కాదా అని తనిఖీ చేస్తుంది.

    పారామీటర్స్:
    వస్తువు: తనిఖీ చేయవలసిన వస్తువు

    రిటర్న్:
    బూలియన్: వస్తువు టెన్సర్ అయితే True, లేకపోతే False
    """
    # టెన్సర్ లక్షణాలను తనిఖీ చేయడం
    if hasattr(వస్తువు, 'ఆకారం') and hasattr(వస్తువు, 'డేటా_రకం') and hasattr(వస్తువు, 'పరికరం'):
        return True
    return False

# ఉదాహరణ వినియోగం
if __name__ == "__main__":
    # టెన్సర్ వంటి వస్తువును సృష్టించడం (నిజమైన టెన్సర్ కాదు)
    class నకిలీ_టెన్సర్:
        def __init__(self):
            self.shape = (2, 3)
            self.dtype = 'float32'
            self.device = 'cpu'

    నకిలీ = నకిలీ_టెన్సర్()
    సాధారణ_జాబితా = [1, 2, 3]

    print(f"నకిలీ టెన్సర్ ఉందా? {టెన్సర్_ఉందా(నకిలీ)}")
    print(f"సాధారణ జాబితా టెన్సర్ ఉందా? {టెన్సర్_ఉందా(సాధారణ_జాబితా)}")
